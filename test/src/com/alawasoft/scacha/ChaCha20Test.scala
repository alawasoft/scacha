package com.alawasoft.scacha

import munit.FunSuite

class ChaCha20Test extends FunSuite {

  // RFC 8439 Section 2.3.2 - ChaCha20 Block Function Test Vector
  test("chachaBlock - RFC 8439 test vector") {
    val key = Array(
      0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
      0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
      0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
      0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f
    ).map(_.toByte)

    val nonce = Array(
      0x00, 0x00, 0x00, 0x09, 0x00, 0x00, 0x00, 0x4a,
      0x00, 0x00, 0x00, 0x00
    ).map(_.toByte)

    val counter = 1

    val result = ChaCha20.chachaBlock(key, counter, nonce)

    val expected = Array(
      0x10, 0xf1, 0xe7, 0xe4, 0xd1, 0x3b, 0x59, 0x15,
      0x50, 0x0f, 0xdd, 0x1f, 0xa3, 0x20, 0x71, 0xc4,
      0xc7, 0xd1, 0xf4, 0xc7, 0x33, 0xc0, 0x68, 0x03,
      0x04, 0x22, 0xaa, 0x9a, 0xc3, 0xd4, 0x6c, 0x4e,
      0xd2, 0x82, 0x64, 0x46, 0x07, 0x9f, 0xaa, 0x09,
      0x14, 0xc2, 0xd7, 0x05, 0xd9, 0x8b, 0x02, 0xa2,
      0xb5, 0x12, 0x9c, 0xd1, 0xde, 0x16, 0x4e, 0xb9,
      0xcb, 0xd0, 0x83, 0xe8, 0xa2, 0x50, 0x3c, 0x4e
    ).map(_.toByte)

    assertEquals(result.toList, expected.toList)
  }

  // RFC 8439 Section 2.4.2 - ChaCha20 Encryption Test Vector
  test("xorStream - RFC 8439 encryption test vector") {
    val key = Array(
      0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
      0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
      0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
      0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f
    ).map(_.toByte)

    val nonce = Array(
      0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x4a,
      0x00, 0x00, 0x00, 0x00
    ).map(_.toByte)

    val plaintext = (
      "Ladies and Gentlemen of the class of '99: " +
      "If I could offer you only one tip for the future, sunscreen would be it."
    ).getBytes("US-ASCII")

    val expected = Array(
      0x6e, 0x2e, 0x35, 0x9a, 0x25, 0x68, 0xf9, 0x80,
      0x41, 0xba, 0x07, 0x28, 0xdd, 0x0d, 0x69, 0x81,
      0xe9, 0x7e, 0x7a, 0xec, 0x1d, 0x43, 0x60, 0xc2,
      0x0a, 0x27, 0xaf, 0xcc, 0xfd, 0x9f, 0xae, 0x0b,
      0xf9, 0x1b, 0x65, 0xc5, 0x52, 0x47, 0x33, 0xab,
      0x8f, 0x59, 0x3d, 0xab, 0xcd, 0x62, 0xb3, 0x57,
      0x16, 0x39, 0xd6, 0x24, 0xe6, 0x51, 0x52, 0xab,
      0x8f, 0x53, 0x0c, 0x35, 0x9f, 0x08, 0x61, 0xd8,
      0x07, 0xca, 0x0d, 0xbf, 0x50, 0x0d, 0x6a, 0x61,
      0x56, 0xa3, 0x8e, 0x08, 0x8a, 0x22, 0xb6, 0x5e,
      0x52, 0xbc, 0x51, 0x4d, 0x16, 0xcc, 0xf8, 0x06,
      0x81, 0x8c, 0xe9, 0x1a, 0xb7, 0x79, 0x37, 0x36,
      0x5a, 0xf9, 0x0b, 0xbf, 0x74, 0xa3, 0x5b, 0xe6,
      0xb4, 0x0b, 0x8e, 0xed, 0xf2, 0x78, 0x5e, 0x42,
