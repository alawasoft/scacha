    val ciphertext = ChaCha20.xorStream(key, nonce, 1, plaintext)
    assertEquals(ciphertext.toList, expected.toList)
  }

  test("xorStream round-trip") {
    val key = new Array[Byte](32)
    key(0) = 0x42
    val nonce = new Array[Byte](12)
    nonce(0) = 0x01
    val plaintext = "Hello, ChaCha20!".getBytes("UTF-8")

    val ciphertext = ChaCha20.xorStream(key, nonce, 0, plaintext)
    val decrypted = ChaCha20.xorStream(key, nonce, 0, ciphertext)
    assertEquals(decrypted.toList, plaintext.toList)
  }

  test("xorStream handles multi-block data") {
    val key = Array.tabulate(32)(i => i.toByte)
    val nonce = Array.tabulate(12)(i => i.toByte)
    val plaintext = Array.tabulate(200)(i => i.toByte) // >3 blocks

    val ciphertext = ChaCha20.xorStream(key, nonce, 0, plaintext)
    assert(ciphertext.length == plaintext.length)
    assert(!ciphertext.sameElements(plaintext))

    val decrypted = ChaCha20.xorStream(key, nonce, 0, ciphertext)
    assertEquals(decrypted.toList, plaintext.toList)
  }
}
